package cau_2;//toán tử pre-increment và post-increment


public class Pre_Post_Demo {
	public static void main (String[] args) {
		int i = 3;
		i++;
		System.out.println(i);
		++i;
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(--i);
		System.out.println(i--);
		System.out.println(i);
	}
}
